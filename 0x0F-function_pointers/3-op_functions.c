#include "3-calc.h"

/**
  * op_add - adds two int
  * @a: first num
  * @b: second num
  * Return: sum of the two
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtracts two int
  * @a: first num
  * @b: second num
  * Return: the result
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - multiply two int
  * @a: first num
  * @b: second num
  * Return: the result
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - divide two int
  * @a: first num
  * @b: second num
  * Return: the result
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - finds the remaider of the division two int
  * @a: first num
  * @b: second num
  * Return: the remainder
  */
int op_mod(int a, int b)
{
	return (a % b);
}

